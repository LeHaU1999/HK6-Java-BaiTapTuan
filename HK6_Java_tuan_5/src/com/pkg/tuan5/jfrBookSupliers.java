    private void tblBookSupliersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBookSupliersMouseClicked
        // TODO add your handling code here:
        txtId.setEnabled(false);
        btnSave.setEnabled(false);
        int i = tblBookSupliers.getSelectedRow();
        TableModel model = tblBookSupliers.getModel();
        txtId.setText(model.getValueAt(i, 0).toString());
        txtName.setText(model.getValueAt(i, 1).toString());
        txtAdress.setText(model.getValueAt(i, 3).toString());
        txtContract.setText(model.getValueAt(i, 4).toString());
        txtEmail.setText(model.getValueAt(i, 5).toString());
        araRemakes.setText(model.getValueAt(i, 6).toString());

        String typeBook = model.getValueAt(i, 2).toString();
        switch (typeBook) {
            case "Books":
                jckBooks.setSelected(true);
                jckJouralsAndMangazines.setSelected(false);
                jckNewspappers.setSelected(false);
                break;
            case "Newspapper":
                jckBooks.setSelected(false);
                jckJouralsAndMangazines.setSelected(false);
                jckNewspappers.setSelected(true);
                break;
            case "Jounrals and Mangazines":
                jckBooks.setSelected(false);
                jckJouralsAndMangazines.setSelected(true);
                jckNewspappers.setSelected(false);
                break;
            case "Books, Newspapper":
                jckBooks.setSelected(true);
                jckJouralsAndMangazines.setSelected(false);
                jckNewspappers.setSelected(true);
                break;
            case "Books, Jounrals and Mangazines":
                jckBooks.setSelected(true);
                jckJouralsAndMangazines.setSelected(true);
                jckNewspappers.setSelected(false);
                break;
            case "Newspapper, Jounrals and Mangazines":
                jckBooks.setSelected(false);
                jckJouralsAndMangazines.setSelected(true);
                jckNewspappers.setSelected(true);
                break;
            case "Books, Newspapper, Jounrals and Mangazines":
                jckBooks.setSelected(true);
                jckJouralsAndMangazines.setSelected(true);
                jckNewspappers.setSelected(true);
                break;
        }
    }//GEN-LAST:event_tblBookSupliersMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        txtId.setEnabled(false);
        String type = null;
         if (jckJouralsAndMangazines.isSelected() && jckBooks.isSelected() && jckNewspappers.isSelected()) {
            type = "Books, Newspapper, Jounrals and Mangazines";
        }else if (jckNewspappers.isSelected() && jckBooks.isSelected()) {
            type = "Books, Newspapper";
        } else if (jckNewspappers.isSelected() && jckJouralsAndMangazines.isSelected()) {
            type = "Newspapper, Jounrals and Mangazines";
        } else if (jckJouralsAndMangazines.isSelected() && jckBooks.isSelected()) {
            type = "Books, Jounrals and Mangazines";
        }else  if (jckBooks.isSelected()) {
            type = "Books";
        }else if (jckNewspappers.isSelected()) {
            type = "Newspapper";
        } else if (jckJouralsAndMangazines.isSelected()) {
            type = "Jounrals and Mangazines";
        }
        
        BookSupliers book = new BookSupliers();
        book.setId((txtId.getText()));
        book.setName((txtName.getText()));
        book.setType(type);
        book.setAddress((txtAdress.getText()));
        book.setContract((txtContract.getText()));
        book.setEmail((txtEmail.getText()));
        book.setRemakes((araRemakes.getText()));

        Controller.addBooks(book);
        JOptionPane.showMessageDialog(null, "Inserted Sucessfully!");
        DefaultTableModel model = (DefaultTableModel) tblBookSupliers.getModel();
        model.setRowCount(0);
        showBookSupliersInTable();

        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

