    private void showBookSupliersInTable() {
        List<BookSupliers> listBooks = Controller.getAllBoosBookSuplierses();
        DefaultTableModel model = (DefaultTableModel) tblBookSupliers.getModel();
        // System.out.println("model");
        Object[] book = new Object[7];
        // System.out.println("oblect");
        for (int i = 0; i < listBooks.size(); i++) {
            //System.out.println("1");
            book[0] = listBooks.get(i).getId();
            // System.out.println("2");
            book[1] = listBooks.get(i).getName();
            //System.out.println("3");
            book[2] = listBooks.get(i).getType();
            //System.out.println("4");
            book[3] = listBooks.get(i).getAddress();
            //System.out.println("5");
            book[4] = listBooks.get(i).getContract();
            //System.out.println("6");
            book[5] = listBooks.get(i).getEmail();
            // System.out.println("7");
            book[6] = listBooks.get(i).getRemakes();
            // System.out.println("8");
            model.addRow(book);
            //System.out.println("9");
        }
    }

