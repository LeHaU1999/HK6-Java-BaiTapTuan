    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(null, "Are You Sure", "DELETE", JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            int rowIndex = tblBookSupliers.getSelectedRow();
            String id = tblBookSupliers.getModel().getValueAt(rowIndex, 0).toString();

            Controller.deleteBooks(id);
            DefaultTableModel model = (DefaultTableModel) tblBookSupliers.getModel();
            model.setRowCount(0);
            JOptionPane.showMessageDialog(null, "Deleted Sucessfully!");
            showBookSupliersInTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

